# string index values

first = "VinayakSinghal"

print(first[1])
print(first[-1]) #last character of string
print(first[-3])
print(first[1:-1]) #start from 1 till -1 (last character not included)
print(first[1:]) # Print till the end

# Some methods return boolean data
print(first.startswith("D"))
print(first.endswith("Z"))
